void main() {
    int idade = 0;
    float altura = 0f;

    Scanner ler = new Scanner(System.in);
    System.out.println("Digite o idade: ");
    idade = ler.nextInt();
    System.out.println("Digite o altura: ");
    altura = ler.nextFloat();

    System.out.println("altura:" + idade);
    System.out.println("altura:" + altura);
}